export function localizeOptions<T extends string>(options: SelectOption<T>[], dictionary: OptionDict<T>): SelectOption<T>[] {
  return options.map((option) => ({
    ...option,
    label: dictionary[option.id]?.label ?? option.label,
    description: dictionary[option.id]?.description ?? option.description,
  }));
}

export function getLocalizedUserPresets(language: Language, presets: readonly { value: number }[]) {
  return presets.map((preset, index) => ({
    ...preset,
    ...userPresetText[language][index],
  }));
}
